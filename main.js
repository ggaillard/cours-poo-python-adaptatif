function exportProgress() {
    if (!courseManager) return;
    
    const data = {
        progress: courseManager.exportStats(),
        timestamp: new Date().toISOString(),
        version: '1.0'
    };
    
    const blob = new Blob([JSON.stringify(data, null, 2)], {
        type: 'application/json'
    });
    
    const url = URL.createObjectURL(blob);
    const a = document.createElement('a');
    a.href = url;
    a.download = `poo-course-progress-${new Date().toISOString().split('T')[0]}.json`;
    a.click();
    
    URL.revokeObjectURL(url);
    
    if (uiManager) {
        uiManager.showNotification('Progression export√©e', 'success');
    }
}

function importProgress() {
    const input = document.createElement('input');
    input.type = 'file';
    input.accept = '.json';
    
    input.onchange = (event) => {
        const file = event.target.files[0];
        if (!file) return;
        
        const reader = new FileReader();
        reader.onload = (e) => {
            try {
                const data = JSON.parse(e.target.result);
                
                if (data.progress && courseManager) {
                    // Ici on pourrait restaurer la progression
                    console.log('Progression import√©e:', data);
                    
                    if (uiManager) {
                        uiManager.showNotification('Progression import√©e', 'success');
                    }
                }
            } catch (error) {
                console.error('Erreur lors de l\'import:', error);
                if (uiManager) {
                    uiManager.showNotification('Erreur lors de l\'import', 'error');
                }
            }
        };
        
        reader.readAsText(file);
    };
    
    input.click();
}

